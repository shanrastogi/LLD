package com.example.adapterdp.models;

public class AdvanceFormat {
    private String fileToPlay;
    private String fileTypeToPlay;
    private String resolution;

    // getter and setter methods
    public String getFileToPlay() {
        return fileToPlay;
    }

    public void setFileToPlay(String fileToPlay) {
        this.fileToPlay = fileToPlay;
    }

    public String getFileTypeToPlay() {
        return fileTypeToPlay;
    }

    public void setFileTypeToPlay(String fileTypeToPlay) {
        this.fileTypeToPlay = fileTypeToPlay;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }
}
